bool setupLeds()
{
    bool doubleBuffer = true;
    if (!ledDisplay.begin(BLEND_FRAMES, TEMPORAL_DITHERING_AMOUNT, DOUBLE_BUFFER, PIO_CONTROLLER)) return false;
    ledDisplay.setBrightness(MAX_DUTY_CYCLE, GAMMA);
    return true;
}

float transition = 0.0;
int loopCount = 0;
int loopCountDirection = 1;

void renderLeds()
{
  int time = millis();
  for(int pixelIndex; pixelIndex < LED_COUNT_PER_PIN; pixelIndex++) 
  {
    if (pixelIndex == 66 || pixelIndex == 79 || pixelIndex == 102 || pixelIndex == 108) continue;
    Color16 color1 = wavePulse(time, pixelIndex, coordsY, coordsX, starter_palette1, 1);//getAudioIntensityRatio());
    Color16 color2 = wavePulse(time, pixelIndex, top_radii, top_angles, starter_palette2, 1);
    Color16 color = blendColorsUsingMixing(color1, color2, transition);
    // Color16 color = wavePulse(time, pixelIndex, top_radii, centered_angles);
    // Color16 color = wavePulse(time, pixelIndex, top_radii, centered_angles);
    // Color16 color = wavePulse(time, pixelIndex, top_radii, centered_angles);
    // Color16 color = wavePulse(time, pixelIndex, top_radii, centered_angles);
    // Color16 color = wavePulse(time, pixelIndex, top_radii, centered_angles);
    ledDisplay.set16(START_LED_OFFSET + pixelIndex, color.red, color.green, color.blue);
  }
  ledDisplay.show();
  loopCount += 1;
  if (loopCount > 2000)
  {
    if (loopCountDirection == 1)
    {
      if (transition < 1) transition = transition + 0.005;
      else {
        loopCountDirection = -1;
        loopCount = 0;
      }
    }
    else 
    {
      if (transition > 0.0) transition = transition - 0.005;
      else {
        loopCountDirection = 1;
        loopCount = 0;
      }
    }
  }
  Serial.println(transition);
}

